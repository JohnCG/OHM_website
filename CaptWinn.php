The Winn House is named after Captain
James Winn who came from a sea-faring family.&nbsp; He was born in
1808 and was the son of James Winn and Philadelphia Maxwell.&nbsp;
In 1846 the Winn family launched a 161&#8211;ton brig out of Kennebunkport
with Captain James at the helm.&nbsp; After several trips he
succumbed to an illness that was aboard the vessel and died at the New
York Marine Hospital.
<br/>
<br/>
After analysis by experts, the house was determined to have been built
around 1780 probably by James Winn Sr.&nbsp; The analysis also
showed evidence of an early 19<sup>th</sup> century restoration.&nbsp; The
central staircase and the front door were added at that time.&nbsp;
The house is built around a central chimney with four
fireplaces.&nbsp; Its other features include wide floor boards,
feather board paneling, interior shutters in the back room, original
windows and a beautiful federal staircase and mantel.&nbsp;
Remarkably. through two hundred years of owners and tenants nothing has
changed the integrity 
of its original architectural
beginnings.&nbsp; The 
house which formerly stood on&nbsp;Route
One north
