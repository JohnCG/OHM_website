to Jacobs Field by the Rotary Club.&nbsp; Committees were formed
and volunteers from the town scraped, painted and researched the
building.&nbsp; In 2001, both the house and the Dorothea Jacobs
Grant Park became a reality.&nbsp; During the original move, &quot;the&nbsp;
ell&quot;&nbsp;or addition to the side of the house became separated
from the main structure.&nbsp; A plan was drawn with the exact
