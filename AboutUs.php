nestled
in the Dorothea Jacob Grant Common, a lovely park with large
open areas and winding paths through wooded areas. Throughout
the cape there remains most of the original paneling, flooring and
hardware.&nbsp; The circa 1780 building was placed on the National
Register of Historic Places in 1979.&nbsp; It is in this historic
setting that you can see exhibits and artifacts that include fishing
and lobstering, the architectural, social and cultural history, the
vibrant art colony and the professional summer theatre that make up
Ogunquit&#8217;s heritage.&nbsp; We welcome and treasure any donations
which are contributed to our archives, and are pleased to share our
knowledge and memories of Ogunquit with Museum guests.&nbsp; Our
staff is dedicated to making visits to the Museum a pleasant and
informative experience.<br/>
