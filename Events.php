<span class="event">Annual Antiques Show:&nbsp;</span>
Our major fundraising event is the Autumn Antiques Show which is
produced by the Friends of the Ogunquit Heritage Museum. This takes
place the second weekend of September on Saturday 10&#8211;5 p.m. and on
Sunday 10&#8211;4 p.m. It is at the S. Judson Dunaway Center at 23 School
Street in Ogunquit and features many dealers from around New England.
The price of admission is $5. You may call 207&#8211;646&#8211;0296 for additional
details. 
