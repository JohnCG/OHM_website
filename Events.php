<span class="event">Christmas by the Sea Open House:&nbsp;</span>
Annually the Ogunquit Chamber of Commerce sponsors a themed
weekend complete with caroling, shopping and a parade. On Saturday and
Sunday afternoons, the Museum is decorated for the season and
refreshments are provided by our committees. It is a chance to meet old
friends, share memories and view our exhibits from the previous summer.
Check for exact times of all events at 
<a href="http://www.ogunquit.org">www.ogunquit.org</a>
 and join us for
Christmas cheer!
