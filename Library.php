The growing collection currently consists of almost 600 volumes of genealogy,
family history, town and regional history, vital records, periodicals
and maps focusing on Maine and other New England states.&nbsp;
There are also approximately 25 linear feet of manuscripts, family
group sheets and biographies.&nbsp;&nbsp; The library has been
supported by the Friends of the Ogunquit Heritage Museum and by
memorial donations in memory of Mr. Seaman.
<br/>
<br/>
Charles Littlefield Seaman (1930&#8211;2003) , a lifetime resident of
Ogunquit, was a direct descendent of Edmund Littlefield, the immigrant
ancestor of the Littlefields in New England and other parts of the
country.&nbsp; Charles&#39; interest in genealogy and history led him
to establish the Littlefield Family Newsletter (a quarterly
genealogical journal) in 1991 and to author three books on Ogunquit
history.&nbsp; The newsletter is carried on today by Peter Woodbury
of Ogunquit.&nbsp; Charles was a member of several genealogical
societies and was a volunteer consultant at the Historical Society of
Wells and Ogunquit for many years.
<br/>
<br/>
The Littlefield Library is open at its permanent location at the
Captain James Winn House during the Museum&#8217;s regular hours.
